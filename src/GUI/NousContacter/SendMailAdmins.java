    private void EnvoyerBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EnvoyerBtnMouseClicked
        if (Message.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "il faut remplir le message");
        }
        else
        {
            try {
                    mail = new Mail(Email.getText());
                    mail.SetContenuMail(Message.getText());
                    mail.envoyerMail();
                    JOptionPane.showMessageDialog(null, "Mail envoy√© ^_^");
                    this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(Avertir.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_EnvoyerBtnMouseClicked

