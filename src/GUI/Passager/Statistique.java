    private void generStat(){
        Passagers c = (Passagers) Session.getInstance().getUser().getCompte();
        int year = Integer.parseInt(filterYear.getValue().toString());
        try {
            affichageStatPanel.removeAll();
            
            if (filterMonth.getSelectedIndex() == 0)
            {
                messageStatLabel.setText("Cout global: "+ PassagersDAO.getInstance().getDeficiteByidPassager(c.getIdPassagers(), year) + " DT");
                affichageStatPanel.add(new ChartPanel(passagerMetier.getCoutByYear(c.getIdPassagers(), year)));
            }
            else
            {
                 messageStatLabel.setText("Cout : "+ PassagersDAO.getInstance().getDeficiteByidPassagerInMonth(c.getIdPassagers(), year, filterMonth.getSelectedIndex()) + " DT");
                affichageStatPanel.add(new ChartPanel(passagerMetier.getCoutByYearAndMonth(c.getIdPassagers(), year ,filterMonth.getSelectedIndex())));
            }
            affichageStatPanel.validate();
        } catch (ProblemeTechniqueException ex) {
            JOptionPane.showMessageDialog(null, "il ya un probleme technique", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }
    
