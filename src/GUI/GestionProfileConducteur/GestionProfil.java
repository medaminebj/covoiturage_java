    private boolean verifierChamps() {
        String erreurMsg = "";
        boolean result = true;

        if (nomTf.getText().length() < 3) {
            erreurMsg += "Le nom doit au moins avoir trois caracteres.\n";
            result = false;
        }

        if (prenomTf.getText().length() < 3) {
            erreurMsg += "Le prenom doit au moins avoir trois caracteres.\n";
            result = false;
        }

        if (!utils.Validators.NumeroTelValidator(numtelTf.getText())) {
            erreurMsg += "Le numéro de téléphone n'est pas valide.\n";
            result = false;
        } else if (!utils.Validators.TailleNumTelValidator(numtelTf.getText())) {
            erreurMsg += "Le numéro de téléphone est supérieur a 8 chiffre .\n";
            result = false;
        } else if (!utils.Validators.NomPrenomValidator(nomTf.getText())) {
            erreurMsg += "Le Nom est invalide.\n";
            result = false;
        } else if (!utils.Validators.NomPrenomValidator(prenomTf.getText())) {
            erreurMsg += "Le Prennom est invalide.\n";
            result = false;
        } else if (!utils.Validators.DateValidator(datedenaissanceTf.getText())) {
            erreurMsg += "Le Format de la date est incorecte .\n";
            result = false;
        } else if (!utils.Validators.ConfirmerMotDePasseValidator(passwordTf.getText(), confirmPasswordTf.getText())) {
            erreurMsg += "la confirmation du mot de passe est inccorecte \n";
            result = false;
        }
        if (!result) {
            JOptionPane.showMessageDialog(null, erreurMsg);
        }

        return result;
    }

    private void RemplissageChamps() {

        try {
            authentification = AuthentificationDAO.getInstance().findById(Entity.Session.getInstance().getUser().getIdAuthentification());

            if ((authentification.getType() == 'c')) {
                conducteur = (Conducteurs) authentification.getCompte();
                nomTf.setText(conducteur.getNom());
                prenomTf.setText(conducteur.getPrenom());
                adresseTf.setText(conducteur.getAdresse());
                numtelTf.setText(conducteur.getNumeroTel());
                emailTf.setText(conducteur.getEmail());
                datedenaissanceTf.setText(conducteur.getDateNaissance().toString());
                if (conducteur.getSexe() == 'M') {
                    sexeTf.setSelectedIndex(0);

                } else {
                    sexeTf.setSelectedIndex(1);
                }
                
            //Get les valeurs a l'entité voiture 
            if (conducteur.getIdVoitures().getIdvoiture()!=0 || conducteur.getIdVoitures()!=null)
            {
                voiture= conducteur.getIdVoitures();
                AnneeTf.setText(String.valueOf(voiture.getAnnee()));
                MarqueTf.setText(voiture.getMarque());
                ModeleTf.setText(voiture.getModele());
                NbrPlaceTf.setText(String.valueOf(voiture.getNbrplaces()));
                
                if(voiture.isEstclimatiser())
                    ClimatiseurBox.setSelected(true);
                else
                     ClimatiseurBox.setSelected(false);

                 if(voiture.isEnfant())
                    EnfantBox.setSelected(true);
                else
                    EnfantBox.setSelected(false);

                  if(voiture.isBagage())
                    BagageBox.setSelected(true);
                else
                    BagageBox.setSelected(false);

                  if(voiture.isTabac())
                   TabacBox.setSelected(true);
                else
                    TabacBox.setSelected(false);
                 // fin 
        
            }
          }

        } catch (ProblemeTechniqueException ex) {
            System.out.println("Probléme de recuperation des données");

        }

    }

