    private void EnregistrerBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EnregistrerBtnMouseClicked
        ModifierBtn.setVisible(true);
        AnnulerBtn.setVisible(true);
        EnregistrerBtn.setVisible(false);
        
        if (verifierChamps()) {
            if (!passwordTf.getText().equals("")) {
                authentification.setPassword(utils.Functions.ConvertToMd5 (passwordTf.getText()));
            }
            
            conducteur.setNom(nomTf.getText());
            conducteur.setPrenom(prenomTf.getText());
            conducteur.setAdresse(adresseTf.getText());
            conducteur.setNumeroTel(numtelTf.getText());
            conducteur.setEmail(emailTf.getText());
            java.text.SimpleDateFormat df = new java.text.SimpleDateFormat("yyyy-MM-dd");

            conducteur.setDateNaissance(new Date(new java.util.Date().getTime()));
            
            System.out.println(conducteur.getDateNaissance());
            
            if (sexeTf.getSelectedIndex()== 0)
            {
                conducteur.setSexe('M');
            }
            else 
                conducteur.setSexe('F');
            //Setter les valeurs a l'entité voiture 
            voiture.setAnnee(Integer.parseInt(AnneeTf.getText()));
            voiture.setMarque(MarqueTf.getText());
            voiture.setModele(ModeleTf.getText());
            voiture.setNbrplaces(Integer.parseInt(NbrPlaceTf.getText()));
            if(ClimatiseurBox.isSelected())
                voiture.setEstclimatiser(true);
            else
                voiture.setEstclimatiser(false);
            
             if(EnfantBox.isSelected())
                voiture.setEnfant(true);
            else
                voiture.setEnfant(false);
             
              if(BagageBox.isSelected())
                voiture.setBagage(true);
            else
                voiture.setBagage(false);
              
              if(TabacBox.isSelected())
                voiture.setTabac(true);
            else
                voiture.setTabac(false);
            // fin 
        }
        try {
                if (AuthentificationDAO.getInstance().update(authentification))
                {
                     if (conducteur.getIdVoitures().getIdvoiture()==0)
                     {
                        if (VoitureDAO.getInstance().create(voiture)) 
                        {
                           JOptionPane.showMessageDialog(null, "La modification de votre profile a été abouti");
                           formaDate.setVisible(false); 
                        }
                     }
                     else
                     {
                        if (VoitureDAO.getInstance().update(voiture)) 
                        {
                           JOptionPane.showMessageDialog(null, "La modification de votre profile a été abouti");
                           formaDate.setVisible(false); 
                        } 
                     }
                     
        
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "La modification n'a pas été abouti");
                }
            
        } catch (ProblemeTechniqueException ex) {
            System.out.println("Probléme de mise à jour ");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GestionProfil.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GestionProfil.class.getName()).log(Level.SEVERE, null, ex);
        }
        AnnulerBtn.setVisible(false);
        setAllDesable();
        
    }//GEN-LAST:event_EnregistrerBtnMouseClicked

